int		ft_count_words_c(char const *s, char c)
{
	int i;
	int nb_words;

	i = 0;
	nb_words = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			nb_words++;
		i++;
	}
	return (nb_words);
}
