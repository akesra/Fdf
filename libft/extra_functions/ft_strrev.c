char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	char	tmp;

	i = 0;
	n = 0;
	while (str[n] != '\0')
		n++;
	n--;
	while (n >= i)
	{
		tmp = str[n];
		str[n] = str[i];
		str[i] = tmp;
		i++;
		n--;
	}
	return (str);
}
